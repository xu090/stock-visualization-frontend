def _infer_market_code(code: str) -> str:
    if code.startswith(("600", "601", "603", "605", "688")):
        return "SH"
    if code.startswith(("000", "001", "002", "003", "300", "301", "302")):
        return "SZ"
    if code.startswith(("430", "830", "831", "832", "833", "835", "836", "837", "838", "839", "871", "873", "920")):
        return "BJ"
    return ""


def bootstrap_default_concepts() -> dict[str, int]:
    current_ids = [concept.id for concept in DEFAULT_CONCEPTS]
    stale_ids = [concept_id for concept_id in LEGACY_DEMO_CONCEPT_IDS if concept_id not in current_ids]
    all_codes = sorted({code for concept in DEFAULT_CONCEPTS for code in concept.stock_codes})

    with get_conn() as conn:
        with conn.cursor() as cur:
            if stale_ids:
                cur.execute("DELETE FROM concept_stocks WHERE concept_id = ANY(%s)", (stale_ids,))
                cur.execute("DELETE FROM concepts WHERE id = ANY(%s)", (stale_ids,))

            for code in all_codes:
                cur.execute(
                    """
                    INSERT INTO stocks (code, name, market_code, is_active, created_at, updated_at)
                    VALUES (%s, %s, %s, TRUE, NOW(), NOW())
                    ON CONFLICT (code) DO UPDATE
                    SET name = COALESCE(NULLIF(EXCLUDED.name, ''), stocks.name),
                        market_code = COALESCE(NULLIF(EXCLUDED.market_code, ''), stocks.market_code),
                        updated_at = NOW()
                    """,
                    (code, get_stock_name(code), _infer_market_code(code)),
                )

            for concept in DEFAULT_CONCEPTS:
                cur.execute(
                    """
                    INSERT INTO concepts (
                        id, name, description, algorithm, editable, favorite, source, created_at, updated_at
                    )
                    VALUES (%s, %s, %s, %s, %s, %s, %s, NOW(), NOW())
                    ON CONFLICT (id) DO UPDATE
                    SET name = EXCLUDED.name,
                        description = EXCLUDED.description,
                        algorithm = EXCLUDED.algorithm,
                        editable = EXCLUDED.editable,
                        favorite = EXCLUDED.favorite,
                        source = EXCLUDED.source,
                        updated_at = NOW()
                    """,
                    (
                        concept.id,
                        concept.name,
                        concept.description,
                        concept.algorithm,
                        concept.editable,
                        concept.favorite,
                        concept.source,
                    ),
                )

            cur.execute("DELETE FROM concept_stocks WHERE concept_id = ANY(%s)", (current_ids,))

            for concept in DEFAULT_CONCEPTS:
                for sort_order, code in enumerate(concept.stock_codes, start=1):
                    cur.execute(
                        """
                        INSERT INTO concept_stocks (concept_id, stock_code, sort_order, created_at)
                        VALUES (%s, %s, %s, NOW())
                        ON CONFLICT (concept_id, stock_code) DO UPDATE
                        SET sort_order = EXCLUDED.sort_order
                        """,
                        (concept.id, code, sort_order),
                    )
        conn.commit()

    return {
        "conceptCount": len(DEFAULT_CONCEPTS),
        "stockCount": len(all_codes),
        "relationCount": sum(len(concept.stock_codes) for concept in DEFAULT_CONCEPTS),
    }


def ensure_default_concepts() -> dict[str, int]:
    current_ids = [concept.id for concept in DEFAULT_CONCEPTS]
    with get_conn() as conn:
        with conn.cursor() as cur:
            cur.execute("SELECT COUNT(*) AS count FROM concepts WHERE id = ANY(%s)", (current_ids,))
            row = cur.fetchone()
            if row and row["count"] == len(current_ids):
                cur.execute("SELECT COUNT(*) AS count FROM concept_stocks WHERE concept_id = ANY(%s)", (current_ids,))
                relation_row = cur.fetchone()
                return {
                    "conceptCount": row["count"],
                    "stockCount": 0,
                    "relationCount": relation_row["count"] if relation_row else 0,
                }

    return bootstrap_default_concepts()
